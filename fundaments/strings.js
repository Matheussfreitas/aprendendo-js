let message = "Estou estudando JavaScript";
console.log(message);

// exibir texto em maiúsculo
console.log(message.toUpperCase());

// exibir texto em minúsculo
console.log(message.toLowerCase());

// comprimento da string
console.log(message.length);

// quantidade de dígitos de um número
let value = 57657;
console.log(value.toString().length);

// substituindo parte de um texto
console.log(message.replace("JavaScript","Python"));

// extraindo uma parte da string (start, end)
console.log(message.slice(0,16));

// extraindo uma parte da string de trás para frente
console.log(message.slice(-10));

// remove espaços em branco no começo e final da string
console.log(message.trim());



